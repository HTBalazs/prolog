#include "pFile.h"

/////////////////////////////////////////////////////////////////////////////////////////
/// Constructor.
/////////////////////////////////////////////////////////////////////////////////////////
pFile::pFile(std::string const& filename, std::string const& mode) {
	file = fopen(filename.c_str(), mode.c_str());
}

/////////////////////////////////////////////////////////////////////////////////////////
/// Destructor.
/////////////////////////////////////////////////////////////////////////////////////////
pFile::~pFile() {
	fclose(file);
}

/////////////////////////////////////////////////////////////////////////////////////////
/// Implements fprintf.
/////////////////////////////////////////////////////////////////////////////////////////
void pFile::fprint(char const* fmt, ...) {
	va_list args;
	va_start(args, fmt);
	vfprintf(file, fmt, args);
	va_end(args);
}

/////////////////////////////////////////////////////////////////////////////////////////
/// Implements vfprintf.
/////////////////////////////////////////////////////////////////////////////////////////
void pFile::vfprint(char const* fmt, va_list args) {
	vfprintf(file,fmt,args);
}
